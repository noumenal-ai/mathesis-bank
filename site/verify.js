/* Verify button — pretty-prints the result of the CI verification workflow.
 * Reads docs/verification.json (produced by ci/verify.sh, refreshed on every
 * GitHub Actions run) and shows the live status + a link to the workflow run.
 * No dependencies, no LLM in this path. */
(function () {
  "use strict";

  // Fallback repo slug for the seed file (before the first CI run fills it).
  // CI sets `repo` in verification.json from $GITHUB_REPOSITORY; that wins.
  const REPO = "noumenal-ai/mathesis";

  const $ = (s, r) => (r || document).querySelector(s);
  const esc = (t) => String(t == null ? "" : t).replace(/[&<>]/g, (c) =>
    ({ "&": "&amp;", "<": "&lt;", ">": "&gt;" }[c]));

  const chip = $("#verifyBtn");
  const panel = $("#verifyPanel");
  if (!chip || !panel) return;

  function ghUrls(data) {
    const repo = (data && data.repo) || REPO;
    const wf = (data && data.workflow) || "verify.yml";
    return {
      badge: `https://github.com/${repo}/actions/workflows/${wf}/badge.svg`,
      workflow: `https://github.com/${repo}/actions/workflows/${wf}`,
      run: (data && data.runUrl) || `https://github.com/${repo}/actions/workflows/${wf}`,
    };
  }

  function setChip(status, text) {
    chip.dataset.status = status || "pending";
    chip.querySelector(".vdot").className = "vdot v-" + (status || "pending");
    chip.querySelector(".vlabel").textContent = text;
  }

  function findVerifyPath() {
    // detail pages live at docs/a/<handle>.html; everything else at docs root
    const depth = location.pathname.includes("/a/") ? "../" : "";
    return depth + "verification.json";
  }

  function render(data) {
    const u = ghUrls(data);
    const when = data.timestamp
      ? new Date(data.timestamp).toLocaleString(undefined, { dateStyle: "medium", timeStyle: "short" })
      : "—";
    const src = data.generatedBy === "ci" ? "GitHub Actions" : "local run (pre-CI seed)";
    const rows = (data.checks || []).map((c) =>
      `<li class="vrow"><span class="vdot v-${esc(c.status)}"></span>
         <div><b>${esc(c.label)}</b><span class="vdetail">${esc(c.detail)}</span></div>
         <span class="vtag vt-${esc(c.status)}">${esc(c.status)}</span></li>`).join("");

    const counts = data.counts || {};
    const countLine = `${esc(counts.schema_valid || "—")} schema-valid &middot; ` +
      `${esc(counts.total_accessions || 0)} accessions &middot; ` +
      `${esc(counts.results || 0)} results`;

    $(".vbody", panel).innerHTML = `
      <div class="vhead">
        <div>
          <div class="vstatus v-${esc(data.status)}">${data.status === "pass" ? "Verified" : esc(data.status)}</div>
          <div class="vmeta">commit <code>${esc(data.commit)}</code> &middot; ${esc(when)} &middot; ${esc(src)}</div>
        </div>
        <a href="${u.workflow}" target="_blank" rel="noopener" class="vbadge-link" title="live workflow status">
          <img alt="verify workflow status" src="${u.badge}"
               onerror="this.style.display='none'">
        </a>
      </div>
      <ul class="vchecks">${rows}</ul>
      <div class="vfoot">
        <span>${countLine}</span>
        <span class="vlinks">
          <a href="${u.run}" target="_blank" rel="noopener">View CI run &rarr;</a>
          <a href="${u.workflow}" target="_blank" rel="noopener">Run verification &rarr;</a>
        </span>
      </div>
      <p class="vnote">This panel reads <code>verification.json</code>, regenerated by the
        <code>verify</code> workflow. It checks that every registry manifest validates against
        the schema, that the recorded whole-theory replay is <code>ADMITTED</code>, that the
        axiom closure is exactly <code>propext</code>, <code>Classical.choice</code>,
        <code>Quot.sound</code>, and that every claim&#8596;result cross-link resolves. No model
        is in this path: this is a byte-faithful re-check of the registry, not a summary.</p>`;

    setChip(data.status, data.status === "pass" ? "Verified" : "Verify");
  }

  function fail(msg) {
    $(".vbody", panel).innerHTML =
      `<p class="vnote">Verification result unavailable (${esc(msg)}). It loads when the page is
       served over HTTP or after the CI workflow has published <code>verification.json</code>.</p>
       <div class="vfoot"><span class="vlinks">
         <a href="https://github.com/${REPO}/actions/workflows/verify.yml" target="_blank" rel="noopener">Open the workflow &rarr;</a>
       </span></div>`;
    setChip("pending", "Verify");
  }

  let loaded = false;
  function load() {
    if (loaded) return Promise.resolve();
    return fetch(findVerifyPath(), { cache: "no-store" })
      .then((r) => (r.ok ? r.json() : Promise.reject(r.status)))
      .then((d) => { loaded = true; render(d); })
      .catch((e) => fail(String(e)));
  }

  function open() { load().finally(() => { panel.hidden = false; chip.setAttribute("aria-expanded", "true"); }); }
  function close() { panel.hidden = true; chip.setAttribute("aria-expanded", "false"); }

  chip.addEventListener("click", () => (panel.hidden ? open() : close()));
  panel.addEventListener("click", (e) => { if (e.target === panel || e.target.classList.contains("vclose")) close(); });
  document.addEventListener("keydown", (e) => { if (e.key === "Escape" && !panel.hidden) close(); });

  fetch(findVerifyPath(), { cache: "no-store" })
    .then((r) => (r.ok ? r.json() : null))
    .then((d) => { if (d) setChip(d.status, d.status === "pass" ? "Verified" : "Verify"); })
    .catch(() => {});
})();
